    private void scannerFilejButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_scannerFilejButtonMouseClicked
        this.monlist.clear();
        this.promptjLabel.setText("");
        //JOptionPane.showMessageDialog(null, this.pokaMain.getLoginUser());
        File guangDianDir = new File(guangDianPath);
        if (!guangDianDir.exists()) {
            guangDianDir.mkdirs();
            return;
        }
        //过滤fsn
        guangDianFiles = guangDianDir.listFiles(new FileFilter() {
            public boolean accept(File file) {
                return file.getName().endsWith(".FSN");
            }
        });
        this.fileNumjLabel.setText(String.valueOf(guangDianFiles.length));
        filesCount = guangDianFiles.length;
        for (int i = 0; i < filesCount; i++) {
            File mfile = guangDianFiles[i];
            String file = mfile.getName();
            String oldFilePath = guangDianPath + File.separator + file;
            File oldFile = new File(oldFilePath);
            String newFilePath = guangDianPath + File.separator + file + ".NICE";
            File newFile = new File(newFilePath);
            if (oldFile.renameTo(newFile)) {
                if (newFile.renameTo(oldFile)) {
                    this.monlist.add(file);
                }
            }
        }
    }//GEN-LAST:event_scannerFilejButtonMouseClicked

