    private void scannerMoneyData() {
        String monVal = "";
        guanZiHaojProgressBar.setValue(0);
        guanZiHaojProgressBar.setOrientation(JProgressBar.HORIZONTAL);
        guanZiHaojProgressBar.setStringPainted(true);
        guanZiHaojProgressBar.setBorderPainted(true);
        guanZiHaojProgressBar.setBackground(Color.pink);
        guanZiHaojProgressBar.setVisible(true);
        guanZiHaojProgressBar.setMaximum(10);
        guanZiHaojProgressBar.setToolTipText("正在转化文件......");
        count = 0;
        String[] aviFiles = this.monlist.getItems();
        isRun = true;  //标示是否在运行中
        for (int i = 0; i < aviFiles.length; i++) {
            String mfile = aviFiles[i];
            String filePath = guangDianPath + File.separator + mfile;
            PokaFsn temR = new PokaFsn();
            try {
                temR.readBaseFsnFile(filePath);   //读取FSN文件信息
            } catch (IOException ex) {
               logger.log(Level.INFO, null,ex);
            }
            List<PokaFsnBody> bd = temR.getbList();
            if (bd.size() != 100) {
                isRightNum = false;
                Object[] options = {"忽略", "退出"};
                int response = JOptionPane.showOptionDialog(this, filePath + "文件冠字号个数不为100条,不满足执行条件", "提示", JOptionPane.YES_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
                if (response == 0) {

                    continue;
                } else if (response == 1) {
                    isRun = false;
                    this.guanZiHaojProgressBar.setVisible(false);
                    return;
                }

            } else {
                String baId = IOUtil.getBaID() + count;
                for (PokaFsnBody mybd : bd) {
                    mybd.setUserId1(args[2]);
                    mybd.setUserId2(args[3]);
                    mybd.setFlag((byte) 0);
                    monVal = "" + mybd.getValuta();
                    pokaFsn.add(mybd);
                    // pokaFsn.getFhead().setCount(pokaFsn.getFhead().getCount() + 1);
                }
                guanZiHaojProgressBar.setValue(guanZiHaojProgressBar.getValue() + 1);
                pokaFsn.addBundleId(baId);
                args[count + 4] = baId;
                count++;
                this.removeFileList.add(new File(filePath));
            }
            if (count == bashu) {
                try {
                    Thread.sleep(500);
                } catch (InterruptedException ex) {
                   logger.log(Level.INFO, null,ex);
                }
                Date date = BundleDeal.getDBTime();
                //捆ID           
                //生成捆ID     
                if (this.xml.getDiaoChaoLogin().equals("1")) {
                    StaticVar.loginUser = qingdianyuanjTextField.getText();
                }
                
                juanbie = this.xml.getQuans(monVal, "" + bashu);
                
                String kunID = BundleDeal.productBKbunId(StaticVar.loginUser, juanbie, 1);
                //生成fsn
                String fsnFileName = StaticVar.bankId + "_" + StaticVar.agencyNo + "_XXXXXXXXXXXXXX_" + kunID + ".FSN";
                String mypath = StaticVar.cfgMap.get(argPro.localAddr) + File.separator + UploadFtp.tem + File.separator + fsnFileName;
                try {
                    pokaFsn.writePokaFsnFile(mypath);
                    pokaFsn = new PokaFsn();
                } catch (Exception ex) {
                   logger.log(Level.INFO, null,ex);
                }
                args[0] = kunID;
                args[1] = new SimpleDateFormat("YYYY-MM-dd HH:mm:ss").format(date);
                //生成bk文件
                String fileName = StaticVar.bankId + "_" + StaticVar.agencyNo + "_XXXXXXXXXXXXXX_" + kunID + (bashu == 5 ? "_05" : "") + ".BK";
                String BKNamePath = StaticVar.cfgMap.get(argPro.localAddr) + File.separator + UploadFtp.tem + File.separator + fileName;
                IOUtil.writeBKFile(args, BKNamePath);   //生成bk文件                                      
                filesCount = filesCount - removeFileList.size();
                this.fileNumjLabel.setText(String.valueOf((filesCount)));
                //备份处理过的文件

                for (File f : removeFileList) {
                    if (f.exists()) {
                        File backFile = new File(fsnbackPath + f.getName());
                        if (backFile.exists()) {
                            backFile = new File(fsnbackPath + f.getName() + "." + (new SimpleDateFormat("yyyyMMddHHmmssSSS")).format(BundleDeal.getDBTime()));
                        }
                        f.renameTo(backFile);
                    }
                    this.monlist.remove(f.getName());
                }
                guanZiHaojProgressBar.setValue(0);
                //上传BK文件
                UploadFtp.oneFileUploadFtp(fileName, UploadFtp.bkbak, (FileUploadMonitor) guanZiHaojProgressBar);
                //上传FSN文件
                UploadFtp.oneFileUploadFtp(fsnFileName, UploadFtp.fsnbak);

                //打印捆封签
                PostekUtil post = new PostekUtil();
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

                String printDate = sdf.format(date);

                String com = (String) this.comComboBox.getSelectedItem();

                post.printWZ(com, printDate, auditer, monVal, inventory, kunID, bankName, bashu);

                int paper = this.lastPaperSizeComboBox.getSelectedIndex();
                if (0 == paper) {
                    post.printWZ(com, printDate, auditer, monVal, inventory, kunID, bankName, bashu);
                } else {
                    post.printGX100X50(com, printDate, auditer, monVal, inventory, kunID, bankName, bashu);
                }

                count = 0;
                removeFileList.clear();
                guanZiHaojProgressBar.setValue(0);
            }

        }
        guanZiHaojProgressBar.setVisible(false);
        if (isRightNum) {
            JOptionPane.showMessageDialog(null, "处理完成.");
        }
        isRun = false;
        this.clearDataButton.setEnabled(true);
        this.bachoice1.setEnabled(true);
    }

